return {
	{
		"rainzm/flash-zh.nvim",
		dependencies = "folke/flash.nvim",
		keys = { "s" },
		config = function()
			require("mapper").map_keymap({ "n", "x", "o" }, "s", function()
				require("flash-zh").jump({ chinese_only = false })
			end, {
				desc = "Jump with flypy",
				condition = function()
					return vim.g.language == "zh"
				end,
				priority = 200,
			})
		end,
	},
}
